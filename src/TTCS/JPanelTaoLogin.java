    private void btnDangKyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDangKyActionPerformed
        // TODO add your handling code here:
        String sql = "SELECT name FROM master.sys.sql_logins";
        Connection con;

        Statement st;
        boolean trungUsername = false;
        try {
            con = DataBaseHelper.getConnection();
            st = con.createStatement();
            ResultSet rs = st.executeQuery(sql);
            while (rs.next()) {
                if (rs.getString(1).equalsIgnoreCase(txtUsername.getText())) {
                    trungUsername = true;
                    break;
                }
            }
            if (trungUsername) {
                txtThongBao.setVisible(true);
                txtThongBao.setText("Tên Đăng Nhập Bị Trùng");
                txtThongBao.setForeground(Color.red);
                
                
            } else {
                // tạo login + set user vào database diem
                sql = "CREATE LOGIN " + txtUsername.getText() + " WITH PASSWORD ='" + txtPassword.getText() + "'; "
                + " EXEC sp_defaultdb @loginame='" + txtUsername.getText() + "' , @defdb='qldiem';"
                + " USE qldiem;"
                + " CREATE USER " + txtUsername.getText() + " for login " + txtUsername.getText() + ";";
                PreparedStatement ps;
                ps = con.prepareStatement(sql);
                ps.execute();
                txtThongBao.setVisible(true);
                txtThongBao.setText("Tạo login thành công");
                txtThongBao.setForeground(Color.BLUE);

            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnDangKyActionPerformed

