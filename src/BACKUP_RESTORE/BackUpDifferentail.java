    private void jButtonChonDuongDanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonChonDuongDanActionPerformed
        // TODO add your handling code here:
        JFileChooser choose = new JFileChooser();
        choose.setCurrentDirectory(new File("C:\\backup"));
        choose.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);

        int x = choose.showDialog(this, "Chọn Folder");
        if(x == JFileChooser.APPROVE_OPTION)
        {
            txtPath.setText(choose.getSelectedFile().getAbsolutePath());

            if(!txtPath.getText().equals("")) txtName.setEnabled(true);
            else
            txtName.setEnabled(false);
        }

    }//GEN-LAST:event_jButtonChonDuongDanActionPerformed

